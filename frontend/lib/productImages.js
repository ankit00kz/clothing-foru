// Get image by category and index
export const getProductImage = (category, index = 0) => {
  const images = productImages[category] || productImages['casual-wear'];
  return images[index % images.length];
};

// Get random image from category
export const getRandomProductImage = (category) => {
  const images = productImages[category] || productImages['casual-wear'];
  return images[Math.floor(Math.random() * images.length)];
};

// Get all images for a category
export const getCategoryImages = (category) => {
  return productImages[category] || [];
};

// Get image by ID
export const getImageById = (category, id) => {
  const images = productImages[category] || [];
  return images.find((img) => img.id === id);
};
